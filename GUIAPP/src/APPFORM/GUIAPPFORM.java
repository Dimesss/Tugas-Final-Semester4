    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        try {
            databaru = false;

            int row = jTable2.getSelectedRow();
            String id = jTable2.getModel().getValueAt(row, 0).toString();

            Connection conn = koneksi.getKoneksi();
            Statement stm = conn.createStatement();
            ResultSet sql = stm.executeQuery("SELECT * FROM absen_siswa WHERE id='" + id + "'");

            if (sql.next()) {
                jTextField1.setText(sql.getString("id"));
                jTextField2.setText(sql.getString("nama"));
                jTextField3.setText(sql.getString("kelas"));
                jTextField4.setText(sql.getString("jenis_kelamin"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(GUIAPPFORM.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jTable2MouseClicked

    private void getData() {
        try {
            Connection conn = koneksi.getKoneksi();
            Statement stm = conn.createStatement();
            ResultSet result = stm.executeQuery("select * from absen_siswa");

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();

            model.setRowCount(0);

            while (result.next()) {
                String ID_Siswa = result.getString("id");
                String Nama = result.getString("nama");
                String Kelas = result.getString("kelas");
                String Jenis_Kelamin = result.getString("jenis_kelamin");

                model.addRow(new Object[]{ID_Siswa, Nama, Kelas, Jenis_Kelamin});
            }

        } catch (SQLException ex) {
            Logger.getLogger(GUIAPPFORM.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

