    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Tombol Save
        if (databaru) {
            try {
                String sql = "INSERT INTO absen_siswa VALUES ('%s', '%s', '%s', '%s')";

                sql = String.format(
                        sql,
                        jTextField1.getText(),
                        jTextField2.getText(),
                        jTextField3.getText(),
                        jTextField4.getText()
                );

                Connection conn = koneksi.getKoneksi();
                PreparedStatement pst = conn.prepareStatement(sql);

                pst.execute();

                JOptionPane.showMessageDialog(null, "Berhasil Disimpan");

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Gagal Disimpan" + ex);
                
            }
        } else {
            try {
                String sql = "UPDATE absen_siswa SET nama='%s', kelas='%s', jenis_kelamin='%s' WHERE id='%s')";

                sql = String.format(
                        sql,
                        jTextField2.getText(),
                        jTextField3.getText(),
                        jTextField4.getText(),
                        jTextField1.getText()
                );

                Connection conn = koneksi.getKoneksi();
                PreparedStatement pst = conn.prepareStatement(sql);

                pst.execute();

                JOptionPane.showMessageDialog(null, "Berhasil");

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Gagal" + ex);
            }
            getData();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Tombol Delete
        try {
            String sql = "DELETE FROM absen_siswa WHERE id = '" + jTextField1.getText() + "'";

            Connection conn = koneksi.getKoneksi();
            PreparedStatement pst = conn.prepareStatement(sql);

            pst.execute();

            JOptionPane.showMessageDialog(null, "Berhasil Dihapus");

            jButton1ActionPerformed(null);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Gagal Dihapus" + ex);
        }
        getData();
    }//GEN-LAST:event_jButton2ActionPerformed

